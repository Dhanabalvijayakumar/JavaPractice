package MyFirstExample;

public class ReverseNumberUsingIfElse {

	public static void main(String[] args) {

		int n =10;
		if(n==10) {
			System.out.println(n--);
		}
		if(n==9) {
			System.out.println(n--);
		}
		if(n==8) {
			System.out.println(n--);
		}
		if(n==7) {
			System.out.println(n--);
		}
		if(n==6) {
			System.out.println(n--);
		}
		if(n==5) {
			System.out.println(n--);
		}
		if(n==4) {
			System.out.println(n--);
		}
		if(n==3) {
			System.out.println(n--);
		}
		if(n==2) {
			System.out.println(n--);
		}
		if(n==1) {
			System.out.println(n--);
		}
		

	}

}
